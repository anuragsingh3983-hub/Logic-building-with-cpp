#include <iostream>
using namespace std;

int main() {
    int intVal = 100;
    float floatVal = 10.5f;
    double doubleVal = 20.123;
    char charVal = 'A';
    bool boolVal = true;
    
    cout << "Integer: " << intVal << endl;
    cout << "Float: " << floatVal << endl;
    cout << "Double: " << doubleVal << endl;
    cout << "Character: " << charVal << endl;
    cout << "Boolean: " << boolVal << endl;
    
    return 0;
}
